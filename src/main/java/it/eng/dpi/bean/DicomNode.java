package it.eng.dpi.bean;

public class DicomNode {

    String hostname;
    int port;
    String aet;

    public DicomNode(String hostname, int port, String aet) {
        super();
        this.hostname = hostname;
        this.port = port;
        this.aet = aet;
    }

    public String getHostname() {
        return hostname;
    }

    public void setHostname(String hostname) {
        this.hostname = hostname;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getAet() {
        return aet;
    }

    public void setAet(String aet) {
        this.aet = aet;
    }

}
